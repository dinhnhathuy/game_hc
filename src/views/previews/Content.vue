          Much like the platformers of that era, Grapple Dog restricts itself to a few simple
          functions and then iterates on them. You can jump (with associated wall-jump and wall-sliding), do a
          mid-air stomp, and, of course, grapple. With only a handful of moves in your arsenal, it's always
